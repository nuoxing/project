package com.jyzx.util;

import java.util.List;

public class FilterParam {
	
	public FilterParam(String name, Object value) {
		this(name, value, "string");
	}
	
	public FilterParam(String name, Object value, String type) {
		this.name = name;
		this.value = value;
		this.type = type;
	}

	public String name;
	public Object value;
	public List<Object> vaflues;
	public String type;
	
	
	public List<Object> getVaflues() {
		return vaflues;
	}

	public void setVaflues(List<Object> vaflues) {
		this.vaflues = vaflues;
	}

	public void setName(String name)
    {
    	this.name = name;
    }
    
    public String getName()
    {
    	return name;
    }
    
    
    public void setValue(Object value)
    {
    	this.value = value;
    }
    
    public Object getValue()
    {
    	return value;
    }
	
    
	public void setType(String type)
    {
    	this.type = type;
    }
    
    public String getType()
    {
    	return type;
    }

	public FilterParam(String name, Object value, List<Object> vaflues,
			String type) {
		this.name = name;
		this.value = value;
		this.vaflues = vaflues;
		this.type = type;
	}
}
